/** 根据梦境文本匹配象征 */
export function interpretDream(text) {
  const results = [];
  const lowerText = text.toLowerCase();
  for (const sym of DREAM_SYMBOLS) {
    const matched = sym.keys.filter(k => lowerText.includes(k));
    if (matched.length > 0) {
      results.push({
        matched: matched[0],
        meaning: sym.meaning,
        reflect: sym.reflect,
        source: sym.source,
      });
    }
  }
  return results;
}
